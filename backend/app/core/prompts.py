"""قوالب الـ prompts المستخدمة مع النموذج.

فصلها هنا يسهّل تعديل صياغة التقييم بلا لمس منطق العمل.
"""
from __future__ import annotations

import json


def build_cv_analysis_prompt(cv_text: str, job_title: str, criteria: dict) -> str:
    criteria_block = json.dumps(criteria["criteria"], ensure_ascii=False, indent=2)

    return f"""أنت مساعد متخصص في تقييم السير الذاتية لفريق الموارد البشرية.
مهمتك: تقييم السيرة الذاتية التالية مقابل معايير محددة، بموضوعية وإنصاف.

المسمى الوظيفي: {job_title}
المجال: {criteria.get('domain_ar', criteria.get('domain'))}

معايير التقييم (كل معيار له وزن ومؤشرات):
{criteria_block}

السيرة الذاتية:
\"\"\"
{cv_text}
\"\"\"

التعليمات:
1. قيّم كل معيار بدرجة من 0 إلى 100 بناءً على ما يظهر فعلاً في السيرة.
2. لا تخمّن معلومات غير موجودة. إذا كان المعيار غير واضح، أعطِ درجة منخفضة واذكر ذلك.
3. اذكر تبريراً موجزاً لكل درجة.
4. تجنّب أي تحيّز يتعلق بالجنس أو الجنسية أو العمر أو العِرق — قيّم الكفاءة فقط.
5. قدّم توصية للـ HR (وليست قراراً نهائياً).

أعد ردك بصيغة JSON فقط، بلا أي نص إضافي أو علامات markdown، بالشكل التالي:
{{
  "scores": [
    {{"key": "...", "score": 0-100, "justification_ar": "..."}}
  ],
  "strengths_ar": ["..."],
  "gaps_ar": ["..."],
  "recommendation_ar": "..."
}}"""


def build_interview_questions_prompt(
    cv_text: str,
    job_title: str,
    domain_ar: str | None,
    analysis_gaps: list[str] | None = None,
) -> str:
    gaps_block = ""
    if analysis_gaps:
        gaps_list = "\n".join(f"- {g}" for g in analysis_gaps)
        gaps_block = f"""
فجوات رصدها تحليل سابق للسيرة الذاتية (استخدمها لصياغة أسئلة تتحقق منها):
{gaps_list}
"""

    return f"""أنت مساعد متخصص في إعداد أسئلة مقابلات لفريق الموارد البشرية.
مهمتك: توليد أسئلة مقابلة مخصصة للمرشح التالي بناءً على خبرته الفعلية الظاهرة في سيرته الذاتية.

المسمى الوظيفي المتقدم له: {job_title}
المجال: {domain_ar or "غير محدد"}
{gaps_block}
السيرة الذاتية:
\"\"\"
{cv_text}
\"\"\"

التعليمات:
1. ولّد من 6 إلى 8 أسئلة مقابلة مرتبطة مباشرة بما ورد في السيرة الذاتية (الخبرات، المهارات، الشهادات، المشاريع).
2. اجعل بعض الأسئلة تتحقق من الفجوات المذكورة أعلاه إن وُجدت.
3. نوّع بين أسئلة تقنية/تخصصية وأسئلة سلوكية (STAR) قصيرة.
4. لا تسأل عن الجنس أو الحالة الاجتماعية أو الديانة أو العمر أو أي معلومة لا علاقة لها بالكفاءة الوظيفية.
5. الأسئلة يجب أن تكون بصيغة نص مباشر قابل للطرح في المقابلة، بدون ترقيم داخل النص نفسه.

أعد ردك بصيغة JSON فقط، بلا أي نص إضافي أو علامات markdown، بالشكل التالي:
{{
  "questions": ["...", "..."]
}}"""


def build_mcq_questions_prompt(
    cv_text: str,
    job_title: str,
    domain_ar: str | None,
    analysis_gaps: list[str] | None = None,
) -> str:
    gaps_block = ""
    if analysis_gaps:
        gaps_list = "\n".join(f"- {g}" for g in analysis_gaps)
        gaps_block = f"""
فجوات رصدها تحليل سابق للسيرة الذاتية (استخدمها لصياغة أسئلة تتحقق منها):
{gaps_list}
"""

    return f"""أنت مساعد متخصص في إعداد اختبارات فرز (screening tests) لفريق الموارد البشرية.
مهمتك: توليد أسئلة اختيار من متعدد (MCQ) لاختبار قصير مرتبط بخبرة المرشح ومجاله الوظيفي.

المسمى الوظيفي المتقدم له: {job_title}
المجال: {domain_ar or "غير محدد"}
{gaps_block}
السيرة الذاتية:
\"\"\"
{cv_text}
\"\"\"

التعليمات:
1. ولّد من 5 إلى 8 أسئلة اختيار من متعدد تختبر معرفة تقنية/تخصصية مرتبطة بالمجال والمسمى الوظيفي، وبما ورد في السيرة الذاتية.
2. كل سؤال له 4 خيارات بالضبط، خيار واحد صحيح فقط.
3. اجعل الخيارات الخاطئة معقولة وليست تافهة بشكل واضح.
4. لا تسأل عن الجنس أو الحالة الاجتماعية أو الديانة أو العمر أو أي معلومة لا علاقة لها بالكفاءة الوظيفية.

أعد ردك بصيغة JSON فقط، بلا أي نص إضافي أو علامات markdown، بالشكل التالي:
{{
  "questions": [
    {{"text": "...", "options": ["...", "...", "...", "..."], "correct_answer": "..."}}
  ]
}}
حيث "correct_answer" يجب أن يطابق نصياً أحد عناصر "options" تماماً."""


def build_voice_evaluation_prompt(
    transcript: list[dict],
    job_title: str,
    domain_ar: str | None = None,
) -> str:
    convo = "\n\n".join(
        f"السؤال: {t['question']}\nإجابة المرشح (منقولة من صوت إلى نص): {t['answer']}"
        for t in transcript
    )

    return f"""أنت مقيّم مقابلات متخصص لفريق الموارد البشرية.
لديك نص محادثة صوتية (حُوّلت من صوت إلى نص تلقائياً) بين مرشح ووكيل ذكاء اصطناعي أجرى معه مقابلة أولية شفهية.

المسمى الوظيفي: {job_title}
المجال: {domain_ar or "غير محدد"}

نص المحادثة:
\"\"\"
{convo}
\"\"\"

التعليمات:
قيّم أداء المرشح في المقابلة الشفهية عبر ستة أبعاد، كل بُعد بدرجة من 0 إلى 100:
1. confidence (الثقة والوضوح): مدى وضوح ويقين الإجابات كما تنعكس من صياغتها اللفظية (تردد أو غموض مقابل يقين ووضوح).
2. communication (مهارات التواصل): قدرته على صياغة الأفكار بوضوح، ترتيب الكلام، استخدام لغة سليمة، ومدى انسيابية الحديث.
3. culture (المعرفة العامة والمهنية): سعة الاطلاع والمعرفة العامة والمهنية المرتبطة بالمجال والوظيفة.
4. understanding (الفهم والاستيعاب): مدى فهمه للأسئلة ومدى دقة وارتباط إجاباته بما سُئل عنه فعلياً.
5. reasoning (المنطق والتحليل): قدرته على التفكير المتسلسل المنطقي وتبرير إجاباته بأمثلة أو أسباب.
6. problem_solving (حل المشكلات): قدرته على اقتراح حلول عملية، التفكير النقدي، والتعامل مع السيناريوهات الافتراضية.

ملاحظة: النص محوّل آليًا من صوت، فقد يحتوي أخطاء بسيطة في التحويل — لا تُحاسب المرشح عليها، ركّز على المحتوى والمعنى.
لا تخمّن معلومات غير موجودة في النص. تجنّب أي تحيّز يتعلق بالجنس أو اللهجة أو الخلفية — قيّم المحتوى فقط.

أعد ردك بصيغة JSON فقط، بلا أي نص إضافي أو علامات markdown، بالشكل التالي:
{{
  "scores": [
    {{"key": "confidence", "score": 0-100, "justification_ar": "..."}},
    {{"key": "communication", "score": 0-100, "justification_ar": "..."}},
    {{"key": "culture", "score": 0-100, "justification_ar": "..."}},
    {{"key": "understanding", "score": 0-100, "justification_ar": "..."}},
    {{"key": "reasoning", "score": 0-100, "justification_ar": "..."}},
    {{"key": "problem_solving", "score": 0-100, "justification_ar": "..."}}
  ],
  "overall_summary_ar": "...",
  "strengths_ar": ["..."],
  "improvement_areas_ar": ["..."]
}}"""


def build_written_test_prompt(items: list[dict], job_title: str, domain_ar: str | None = None) -> str:
    qa_block = "\n\n".join(
        f"[{i}] السؤال: {it['question']}\nإجابة المرشح (مكتوبة يدويًا في اختبار ورقي): {it['answer']}"
        for i, it in enumerate(items)
    )

    return f"""أنت مصحّح اختبارات متخصص لفريق الموارد البشرية.
لديك إجابات مكتوبة يدويًا لمرشح على أسئلة مقابلة مفتوحة في اختبار ورقي، وأدخلها فريق HR نصيًا كما هي.

المسمى الوظيفي: {job_title}
المجال: {domain_ar or "غير محدد"}

الأسئلة والإجابات:
\"\"\"
{qa_block}
\"\"\"

التعليمات:
قيّم كل إجابة على حدة بدرجة من 0 إلى 100 بناءً على دقتها وصحتها ومدى ارتباطها بالسؤال، مع تبرير موجز لكل درجة.
لا تخمّن معلومات غير موجودة. تجنّب أي تحيّز يتعلق بالجنس أو الخلفية — قيّم المحتوى فقط.

أعد ردك بصيغة JSON فقط، بلا أي نص إضافي أو علامات markdown، بالشكل التالي:
{{
  "results": [
    {{"index": 0, "score": 0-100, "justification_ar": "..."}}
  ]
}}
حيث "index" يطابق الرقم بين قوسين [ ] لكل سؤال أعلاه بالضبط."""


def build_final_recommendation_prompt(
    job_title: str,
    domain_ar: str | None,
    cv_result: dict | None,
    voice_result: dict | None,
    written_result: dict | None,
) -> str:
    sections = []

    if cv_result:
        sections.append(
            "تحليل السيرة الذاتية:\n"
            f"- الدرجة الإجمالية: {cv_result.get('overall_score')}/100\n"
            f"- نقاط القوة: {', '.join(cv_result.get('strengths_ar', [])) or 'لا يوجد'}\n"
            f"- الفجوات: {', '.join(cv_result.get('gaps_ar', [])) or 'لا يوجد'}"
        )

    if voice_result:
        voice_scores = "، ".join(
            f"{s['label_ar']}: {s['score']}/100" for s in voice_result.get("scores", [])
        )
        sections.append(
            "المقابلة الصوتية:\n"
            f"- الدرجات: {voice_scores}\n"
            f"- الملخص: {voice_result.get('overall_summary_ar', '')}"
        )

    if written_result:
        sections.append(
            "الاختبار الورقي المكتوب:\n"
            f"- الدرجة الإجمالية: {written_result.get('overall_score')}/100\n"
            + "\n".join(
                f"  * {a['question_text']}: {a.get('score', '؟')}/100"
                + (f" — {a['justification_ar']}" if a.get("justification_ar") else "")
                for a in written_result.get("answers", [])
            )
        )

    evidence_block = "\n\n".join(sections) if sections else "لا تتوفر بيانات تقييم كافية بعد."

    return f"""أنت مستشار توظيف كبير لفريق الموارد البشرية.
لديك نتائج تقييم مرشح لوظيفة معينة من مصادر متعددة (سيرة ذاتية، مقابلة صوتية، اختبار ورقي — بعضها قد يكون غير متوفر).

المسمى الوظيفي: {job_title}
المجال: {domain_ar or "غير محدد"}

بيانات التقييم المتوفرة:
\"\"\"
{evidence_block}
\"\"\"

التعليمات:
1. لخّص أبرز نقاط القوة لدى المرشح (لا تتجاوز 5 نقاط).
2. لخّص أبرز نقاط الضعف أو الفجوات (لا تتجاوز 5 نقاط).
3. بناءً على كل البيانات المتوفرة فقط، أعطِ رأيك: هل تُرشّح هذا المرشح للمرحلة التالية أم لا، مع توضيح السبب بوضوح.
4. هذه توصية استرشادية للـ HR فقط — القرار النهائي دائمًا للإنسان.
5. تجنّب أي تحيّز يتعلق بالجنس أو الجنسية أو العمر أو العِرق — قيّم الكفاءة فقط.

أعد ردك بصيغة JSON فقط، بلا أي نص إضافي أو علامات markdown، بالشكل التالي:
{{
  "recommend": true أو false,
  "reason_ar": "...",
  "strengths_ar": ["..."],
  "weaknesses_ar": ["..."]
}}"""
